public class PosNegCheck {

	public static void main(String[] args) {
		int n = -12;
		if (n > 0) {
			System.out.println("The no. " + n + " is positive no. ");
		} else if (n < 0) {
			System.out.println("The no. " + n + " is negative no. ");
		} else if (n == 0) {
			System.out.println("The no. " + n + " is neither positive nor negative ");
		}

	}
}
